import sys
sys.path.append('..')
from repo import config as CONFIG
from gensim.models.word2vec import Word2Vec
from sklearn.preprocessing import normalize
import numpy as np
import csv
import pickle


w2v_model_path = CONFIG.datasets_path + "GoogleNews-vectors-negative300.bin"
w2v_model = Word2Vec.load_word2vec_format(w2v_model_path, binary=True)

with open(CONFIG.wordsim_path + "combined.csv", 'r') as f:
  reader = csv.reader(f)
  next(reader, None)
  wordsim = list(reader)

w2v_res = []
for w1,w2,avg in wordsim:
  w1_vector = normalize(np.array([w2v_model[w1]]),axis=1)[0]
  w2_vector = normalize(np.array([w2v_model[w2]]),axis=1)[0]
  res = np.matmul(w1_vector,np.transpose(w2_vector))
  w2v_res.append({'w1': w1, 'w2': w2, 'human_dist': avg, 'w2v_dist': res})

with open(CONFIG.wordsim_path + "combined_with_w2v.p", 'wb') as file:
  pickle.dump(w2v_res, file, protocol=pickle.HIGHEST_PROTOCOL)

